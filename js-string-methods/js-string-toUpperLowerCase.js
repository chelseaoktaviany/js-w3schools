/**
 *
 * Converting to Upper and Lower Case
 *
 */

// A string is converted to uppercase with toUpperCase()
let text1 = "Hello World!";
let text2 = text1.toUpperCase();

console.log(text2); //the output will be "HELLO WORLD!"

// A string is converted to lowercase with toLowerCase()
let text3 = "Hello World!";
let text4 = text3.toLowerCase();

console.log(text4); //the output will be "hello world!"
