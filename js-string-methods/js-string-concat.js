/**
 *
 * JavaScript String concat()
 *
 */

//concat() joins two or more strings:
let text1 = "Hello";
let text2 = "World";
let text3 = text1.concat(" ", text2);

console.log(text3); //the output will be "Hello World"

// another example for more strings
let text4 = "Chelsea";
let text5 = "Oktaviany";
let text6 = "hehe";
let text7 = text4.concat(" ", text5, " ", text6);

console.log(text7); //the output will be "Chelsea Oktaviany hehe"
