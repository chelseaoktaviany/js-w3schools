// JavaScript Numbers Basic
// Numbers can be written with or without decimals:
let x = 3.14;
let y = 3;

console.log(x); // 3.14
console.log(y); // 3

//extra large or extra small numbers can be written with scientific (exponent) notation:
let x2 = 123e5;
let y2 = 123e-5;

console.log(x2); // 12300000
console.log(y2); // 0.00123