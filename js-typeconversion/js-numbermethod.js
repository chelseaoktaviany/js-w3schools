// the number() method converts a variable (or value) into a number:
console.log(Number("3.14") + "\n");     // 3.14
console.log(Number(Math.PI) + "\n");    // 3.141592653589793
console.log(Number("    ") + "\n");     // 0
console.log(Number("") + "\n");         // 0
console.log(Number("99 88") + "\n");    // NaN
console.log(Number("John") + "\n");     // NaN