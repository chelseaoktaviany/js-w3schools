/**
 * Math.min() and Math.max() can be used to find the lowest or highest value in a list of arguments:
 */

//examples
let min = Math.min(0, 150, 30, 20, -8, -200);
console.log("The lowest value in a list is ", min);     // the lowest value in a list is -200

let max = Math.max(0, 150, 30, 20, -8, -200);
console.log("The highest value in a list is ", max);    // the highest value in a list is 150