/**
 * Math.pow(x,y) returns the value of x to the power of y (pangkat)
 * This can also return the negative value
 */

//Example
console.log(Math.pow(8, 2));    // 64
console.log(Math.pow(12,2));    // 144
console.log(Math.pow(-9, 3));   // -729
console.log(Math.pow(13, 2));   // 169
console.log(Math.pow(-18, 2));  // 324
console.log(Math.pow(-19, 3))   // -6859