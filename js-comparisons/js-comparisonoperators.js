// comparison operators are used in logical statement to determine equality or difference between variables or values

let x = 5;

// == operator
console.log((x == 8));      //false
console.log((x == 5));      //true
console.log((x == "5"));    //true

// === operator
console.log((x === 5));     //true
console.log((x === "5"));   //false

// != operator
console.log((x != 8));      //true

// !== operator
console.log((x !== 5));     //false
console.log((x !== "5"));   //true
console.log((x !== 8));     //true

// > operator
console.log((x > 8));       //false

// < operator
console.log((x < 8));       //true

// >= operator
console.log((x >= 8));      //false

// <= operator
console.log((x <= 8));      //true