//arrays are objects
/**
 * Arrays are a special type of 
 * objects. The typeof operator in 
 * JavaScript returns "object" for 
 * arrays. But, JavaScript arrays are best described as arrays. 
 * 
 */

//Arrays use numbers to access its "elements". In this example, person[0] returns John:
const person = ["John", "Doe", 46];
console.log(person[0]); //John

//objects use names to access its "members". in this example, person.firstName returns john:
const person2 = {firstName: "Xavier", lastName: "Desmond", age: 20};
console.log(person2.firstName); //Xavier

