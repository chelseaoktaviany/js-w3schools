/**
 * Math.sin() - returns the sine (a value between -1 and 1) of the angle x (given in radians).
 */

//example
let deg = Math.sin(90 * Math.PI / 180);
console.log("The sine value of 90 degrees is ", deg); // The sine value of 90 degrees is 1

let deg2 = Math.sin(-270 * Math.PI / 180);
console.log("The sine value of -270 degrees is ", deg2); // The sine value of -270 degrees is 1